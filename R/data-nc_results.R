#' North Carolina election results (precinct, county, and state levels)
#'
#' Cleaned candidate/choice-level election results for North Carolina produced by
#' the package's North Carolina pipeline.
#'
#' @format
#' \describe{
#'   \item{nc_precinct_results}{A data frame with precinct-level results.}
#'   \item{nc_county_results}{A data frame with county-level results.}
#'   \item{nc_state_results}{A data frame with state-level results.}
#' }
#'
#' @details
#' These datasets are included as package data objects. They can be loaded with
#' \code{data(nc_precinct_results)}, \code{data(nc_county_results)}, and
#' \code{data(nc_state_results)}.
#'
#' All three datasets include a consistent set of core variables such as
#' \code{state}, \code{election_date}, \code{contest_name}, \code{choice}, and
#' vote totals (e.g., \code{election_day}, \code{early_voting},
#' \code{absentee_by_mail}, \code{provisional}, \code{total_votes}).
#'
#' @section Variables:
#' \describe{
#'   \item{state}{Two-letter postal abbreviation (e.g., \code{"NC"}).}
#'   \item{county}{County name (county/precinct datasets only).}
#'   \item{precinct}{Precinct name/identifier (precinct dataset only).}
#'   \item{election_date}{Election date as \code{Date}.}
#'   \item{election_type}{Election type label (precinct dataset only).}
#'   \item{contest_name}{Contest/race name.}
#'   \item{contest_group_id}{Contest grouping identifier (precinct dataset only).}
#'   \item{contest_type}{Contest type label (precinct dataset only).}
#'   \item{jurisdiction}{Jurisdiction label.}
#'   \item{office}{Office name.}
#'   \item{district}{District identifier/label.}
#'   \item{choice}{Candidate/option name.}
#'   \item{choice_party}{Party for the choice/candidate, when available.}
#'   \item{vote_for}{Number of seats/votes per voter (precinct dataset only).}
#'   \item{election_day}{Votes cast on election day.}
#'   \item{early_voting}{Votes cast during early voting.}
#'   \item{absentee_by_mail}{Absentee-by-mail votes.}
#'   \item{absentee_or_early}{Combined absentee/early category, when provided.}
#'   \item{provisional}{Provisional votes.}
#'   \item{total_votes}{Total votes for the choice.}
#'   \item{runoff_status}{Runoff status label (precinct dataset only).}
#'   \item{recount_status}{Recount status label (precinct dataset only).}
#'   \item{winner_status}{Winner status label (precinct dataset only).}
#'   \item{real_precinct}{Normalized/cleaned precinct name (precinct dataset only).}
#'   \item{precinct_abbrv}{Precinct abbreviation (precinct dataset only).}
#'   \item{ftp_date}{Source file date from FTP, when available (precinct dataset only).}
#'   \item{election_year}{Election year (precinct dataset only).}
#'   \item{vote_share}{Vote share for the choice (state dataset only).}
#'   \item{contest_outcome}{Contest outcome label (state dataset only).}
#' }
#'
#' @source
#' Generated by the package data build script \code{data-raw/build_nc_snapshot.R}.
#'
#' @name nc_results
#' @aliases nc_precinct_results nc_county_results nc_state_results
#' @docType data
